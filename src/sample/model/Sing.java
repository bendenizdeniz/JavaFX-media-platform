package sample.model;

public class Sing {
    private int IDSing;
    private String Name;
    private int SingAlbumID;

    public int getIDSing() {
        return IDSing;
    }

    public void setIDSing(int IDSing) {
        this.IDSing = IDSing;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public int getSingAlbumID() {
        return SingAlbumID;
    }

    public void setSingAlbumID(int singAlbumID) {
        SingAlbumID = singAlbumID;
    }
}
